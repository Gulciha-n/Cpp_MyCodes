#include <iostream>

using namespace std;

int main(){

    //for
    for (int i=1 ; i<=20 ; i++){
        cout << i << endl;
    }

    for (int a=1 ; a<=20 ; a++){
        if(a % 2 == 0)
            cout << a << endl;
    }
    
    //while
    int j = 1;
    while(j<=20){
        cout << j << endl;
        j++;
    }
} 
